const month = "March"

switch (month) {
    case "Jan":
        console.log("Jan");
        break;
    case "Feb":
        console.log("Feb");
        break;
    case "March":
        console.log("March");
        break;
    case "April":
        console.log("April");
        break;
    case "May":
        console.log("May");
        break;
    
    default:
        console.log("Default case match");
        break;
}